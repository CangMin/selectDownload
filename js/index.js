$(document).ready(function(){
	
	$(".icon-select").mouseover(function(){
		$("#options").css("display","block");
	});
	$("#options").mouseover(function(){
		$("#options").css("display","block");
	});
	$(".icon-select").mouseout(function(){
		$("#options").css("display","none");
	});
	$("#options").mouseout(function(){
		$("#options").css("display","none");
	});
	});